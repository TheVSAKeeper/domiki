export function getDomikIncidentTemplate(templateId: number): DomikIncidentTemplate {
    return templates[Math.abs(templateId) % templates.length] ?? template0;
}

export function domikIncidentText(text: string, domikName: string, heroName: string, heroGender?: number): string {
    return text.replaceAll('{домик}', domikName).replaceAll('{имя}', heroName).replace(/\{([^{}|]*)\|([^{}|]*)\}/g, (_, male: string, female: string) => genderForm(heroGender, male, female));
}
