export function getIncidentTemplate(templateId: number): IncidentTemplate {
    return templates[Math.abs(templateId) % templates.length] ?? template0;
}

export function incidentText(text: string, workerName: string, workerGender: number | undefined): string {
    return text.replaceAll('{имя}', workerName).replace(/\{([^{}|]*)\|([^{}|]*)\}/g, (_, male: string, female: string) => genderForm(workerGender, male, female));
}
