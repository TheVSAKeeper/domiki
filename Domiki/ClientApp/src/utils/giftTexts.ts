const fallback: GiftTextSet = {
  regular: [
    'Кто-то из соседей заглядывал, пока вас не было, – оставил гостинец у ворот.',
    'На воротах записка: «Заходили, не застали. Гостинец – под навесом. Соседи».',
  ],
  big: [
    'Соседи сговорились и оставили вам подарок – славный, от всей округи.',
  ],
};

export function pickGiftText(neighborId: number, big: boolean, seed: string): string {
  const set = byNeighbor[neighborId] ?? fallback;
  const pool = big ? set.big : set.regular;
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    hash = (hash * 31 + seed.charCodeAt(i)) | 0;
  }
  return pool[Math.abs(hash) % pool.length] ?? pool[0] ?? '';
}
